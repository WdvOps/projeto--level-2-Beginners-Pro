function showModal() {
    let element = document.querySelector(".modal-container");
    element.style.display = "flex";
}
function closeModal() {
    let element = document.querySelector(".modal-container");
    element.style.display = "none";
}
function openPhoto() {
    let element = document.querySelector(".photo-galery");
    element.style.display = "flex";
}
function closePhoto() {
    let element = document.querySelector(".photo-galery");
    element.style.display = "none";
}